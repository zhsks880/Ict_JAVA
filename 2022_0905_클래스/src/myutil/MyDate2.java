package myutil;

public class MyDate2 {
	
	//은닉화(data hiding): 접근제한자 private을 이용해서
	private int year;
	private int month;
	private int day;
	
	//알트 + 쉬프트 + s = 코드자동생성 단축키
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	
	
	
	
	
	
}
